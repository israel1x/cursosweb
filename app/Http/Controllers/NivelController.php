    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Curso $curso,Request $request)
    {
        $nivel = new Nivel();
        $nivel->descripcion = $request->input('descripcion');
        $nivel->precio = $request->input('precio');

        $nivel->estado = $request->input('estado');
        $nivel->IdUsuarioModificacion = 1;
        $nivel->curso_id()->associate($curso)->save();

        //$nivel->save();

        return redirect()->route('nivel.index')->withStatus(__('Nivel Creado.'));
    }

